import turtle
turtle.Screen().bgcolor("blue")
turtle.Screen().setup(500,500)
ab=turtle.Turtle()

ab.forward(100)
ab.left(120)

ab.forward(100)
ab.left(120)

ab.forward(100)
ab.penup()

ab.right(150)
ab.forward(50)

ab.pendown()
ab.right(90)

ab.forward(100)
ab.right(120)

ab.forward(100)
ab.right(120)

ab.forward(100)
turtle.done()